// Dodawanie
export function add1(numberA, numberB)
{
    return numberA + numberB;
}
const result1 = add1(3, 4);
console.log("Wynik dodawania:", result1);


//Odejmowanie
export function add2(numberC, numberD)
{
    return numberC - numberD;
}
const result2 = add2(4, 4);
console.log("Wynik odejmowania:", result2);


//Mnożenie
export function add3(numberE, numberF)
{
    return numberE * numberF;
}
const result3 = add3(5, 4);
console.log("Wynik mnożenia:", result3);


//Dzielenie
export function add4(numberG, numberH)
{
    return numberG / numberH;
}
const result4 = add4(16, 4);
console.log("Wynik dzielenia:", result4);